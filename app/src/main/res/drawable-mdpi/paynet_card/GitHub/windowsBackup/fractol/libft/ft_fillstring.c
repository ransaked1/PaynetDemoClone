#include "libft.h"

char	*ft_fillstring(char *str, int n, int x, int s)
{
	while (x >= 0)
	{
		if (n < 0)
			n = -n;
		str[x] = n % 10 + '0';
		n = n / 10;
		if (x == 0 && s == 1)
			str[x] = '-';
		x--;
	}
	return (str);
}
